# Écrire un programme Python qui permet d'afficher si un nombre entier saisi au clavier est pair ou impair. 

# on demmande à l'utilisateur de saisir un entier
n=int(input("donner un entier n : "))

# on effectue des condition pour verifié la parité de ce nombre

if n%2==0:
    print(f"le nombre {n} est pair")
else :
    print(f"le nombre {n} est impair")


